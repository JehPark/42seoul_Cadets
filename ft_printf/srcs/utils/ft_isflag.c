#include "ft_printf.h"

int   ft_isflag(char ch)
{
  if (ch == '%' || ch == 'c' || ch == 's'
      || ch == 'i' || ch == 'd' || ch == 'u'
      || ch == 'p' || ch == 'x' || ch == 'X')
      return (1);
	return (0);
}